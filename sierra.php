      <h1>La Sierra, The Mighty Andes</h1>
      <p>The Ecuadorian Andes region, commonly known as &quot;La Sierra&quot;, includes ten provinces, each with its own distinct flavor and culture diversity. The Andean Highlands cover only 20% of the surface of Ecuador, but are home to nearly 50% of the Ecuador population, most of them in the UNESCO cultural heritage sites of Quito and Cuenca, both striking colonial cities. <br />
        <br />
      </p>
      <p>We have an assortment of day trips that we like to include into the beginning of your stay in Ecuador. Through these quick trips you will be able to ease into Ecuador and gain a feel for the country before we delve further into the country's wonders!</p>
      <br />
      <h3>Day Trips</h3>
      <ul>
        <li><a href="http://www.papallacta.com.ec/"><strong>Papallacta</strong></a> hot springs.</li>
        <li><a href="http://en.wikipedia.org/wiki/Mitad_del_Mundo"><strong>Mitad del Mundo</strong></a>.</li>
        <li><a href="http://www.banios.com/"><strong>Baños</strong></a>, a resort village at the foot of the active volcano Tunguragua.</li>
        <li><a href="http://en.wikipedia.org/wiki/Riobamba"><strong>Riobamba</strong></a>, Ecuador's first capital and origin of the breathtaking, world renowned Nariz del Diablo (Nose of the Devil) train ride, an engineering feat in its day (early 19th century).</li>
        <li><a href="http://en.wikipedia.org/wiki/Mindo,_Ecuador"><strong>Mindo</strong></a>, a bird watching paradise nestled in the Cloud Forest with a vast array of species including humming birds, toucans, the brilliant cock-of-the-rock, as well as orchids and butterflies.</li>
        <li><a href="http://en.wikipedia.org/wiki/Quilotoa"><strong>Quilotoa</strong></a> and <a href="http://www.ecostravel.com/en/ecuador/cities-destination/guaranda/"><strong>Salinas de Guaranda</strong></a>, spectacular and colorful indigenous communities with breathtaking handicrafts at extraordinary prices.</li>
        <li><a href="http://en.wikipedia.org/wiki/Otavalo"><strong>Otavalo, Cotacachi and San Antonio de Ibarra</strong></a>, Latin America's top destinations for textiles, leather goods, and wood carvings.</li>
